    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String kodeBarang = jTextField1.getText().trim();
    String namaBarang = jTextField2.getText().trim();
    String kategori = jTextField3.getText().trim();
    String harga_beli = jTextField4.getText().trim();
    String harga_jual = jTextField5.getText().trim();
    String stok = jTextField6.getText().trim();
    
    Connection conn = null;
    try {
        conn = DatabaseConnection.connect(); 
        String sql = "INSERT INTO barang (kode_barang, nama_barang, kategori, harga_beli, harga_jual, stok) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, kodeBarang);
        stmt.setString(2, namaBarang);
        stmt.setString(3, kategori);
        stmt.setString(4, harga_beli);
        stmt.setString(5, harga_jual);
        stmt.setString(6, stok);
        stmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data Stok Barang berhasil disimpan.");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        tampilkanDataInputBarang();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

