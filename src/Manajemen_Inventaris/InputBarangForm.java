private void tampilkanDataInputBarang(){
    try{
        Connection conn = DatabaseConnection.connect();
        String sql = "SELECT id, kode_barang, nama_barang, kategori, harga_beli, harga_jual, stok FROM barang";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Kode Brg");
        model.addColumn("Nama Brg");
        model.addColumn("Kategori");
        model.addColumn("Harga Beli");
        model.addColumn("Harga Jual");
        model.addColumn("Stok");
        
        while (rs.next()){
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getString("kategori"),
                rs.getString("harga_beli"),
                rs.getString("harga_jual"),
                rs.getString("stok")
            });
        }
        
        jTable1.setModel(model);
        lblKodeBarang = new javax.swing.JLabel("Kode Barang:");
        txtKodeBarang = new javax.swing.JTextField();
    }catch (Exception e){
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
