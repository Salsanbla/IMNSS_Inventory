    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText().trim();
        String password = jPasswordField1.getText().trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan password wajib diisi!");
            return;
        }
        
        Connection conn = null;
        try {
            conn = DatabaseConnection.connect();
            String query = "SELECT * FROM users WHERE username = ? AND password = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String role = rs.getString("role");
                String roleNormalized = role.trim().toLowerCase();
                System.out.println("Role dari DB: [" + role + "] â†’ [" + roleNormalized + "]");

                JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + role);

                switch (roleNormalized) {
                    case "admin":                       
                        new Dashboard_Admin().setVisible(true);
                        break;
                    case "kasir":
                    case "crew":
                        new MainMenu(roleNormalized).setVisible(true);
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Role tidak dikenali: " + role);
                        return;
                }

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username atau password salah!");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Kesalahan koneksi database: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

